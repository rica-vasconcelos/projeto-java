package br.com.pesquisa.model;

public class Cep {

	private int idCep;
	private int cep;
	private String logradouro;
	public int getIdCep() {
		return idCep;
	}
	public void setIdCep(int idCep) {
		this.idCep = idCep;
	}
	public int getCep() {
		return cep;
	}
	public void setCep(int cep) {
		this.cep = cep;
	}
	public String getLogradouro() {
		return logradouro;
	}
	public void setLogradouro(String logradouro) {
		this.logradouro = logradouro;
	}
}
